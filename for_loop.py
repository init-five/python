# for loop
obj = [1, 2, 3, 5, 6, 8, 10]
for value in obj:
    print(value)
    # print multiple of 2 of values
    print(value * 2)
# print sum of first 5 natural numbers
sum = 0
for num in range(1, 6):
    # print first 5 numbers from 1 - 5
    print(num)
    # print sum of 1 - 5 numbers
    sum += num
print(sum)
# print with 2 indexes difference
for num2 in range(1,10, 2):
    print(num2)
# start from 0 to n-1
for n in range(10):
    print(n)
    